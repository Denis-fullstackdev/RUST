fn main() {

    let n = 30;

    if n < 30 {
        println!("Value of n is less then 30!");
    }

    if n == 30 {
        println!("Value of n is equal 30!");
    }

    if n > 30 {
        println!("Value of n is greater then 30!");
    }

    if n != 30 {
        println!("Value of n is NOT equal 30!");
    } else {
        println!("Value of n is equal 30! (Using else)");
    }

}
